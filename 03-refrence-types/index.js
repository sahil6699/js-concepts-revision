//datatypes
//Reference types
    // Array
    // function
    // object 
        // when we deal with multipple related variable we declare them in an object
let person = {
    name: 'sahil',
    age: 22
};

console.log(person);

//acces the properties
//Dot notation
person.name = "John";
console.log(person.name);

//bracket notation
let selection = 'name';
person[selection] = "Mary";
console.log(person.name);

